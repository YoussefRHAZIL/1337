int	ft_is_prime(int nb)
{
	long int	i;
	long int	nbr;

	i = 2;
	nbr = nb;
	if (nbr <= 1)
		return (0);
	if (nbr <= 3)
		return (1);
	while (i <= nbr / i)
	{
		if (nbr % i == 0)
			return (0);
		i++;
	}
	return (1);
}
