int	ft_sqrt(int nb)
{
	long	i;
	long	nbr;

	nbr = nb;
	i = 2;
	if (nbr <= 0)
		return (0);
	else if (nbr == 1)
		return (1);
	else
	{
		while (i * i <= nbr)
		{
			if (i * i == nbr)
				return (i);
			i++;
		}
	}
	return (0);
}
