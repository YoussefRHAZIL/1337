#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	p;

	i = 1;
	p = 1;
	if (power < 0)
	{
		return (0);
	}
	if (nb == 0 && power == 0)
	{
		return (1);
	}
	while (i <= power)
	{
		p = p * nb;
		i++;
	}
	return (p);
}
